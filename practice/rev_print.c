#include <unistd.h>

int		main(int args, char **argv)
{
	int i;

	args = 0;
	i = 0;
	while (argv[1][i])
	{
		i++;
	}
	i--;
	while (argv[1][i])
	{
		write(1, &argv[1][i], 1);
		i--;
	}
	return (0);
}